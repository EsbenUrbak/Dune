package com.dune.entities;


public class Path {

	
	private int x;
	private int y;
	private boolean active;
	
public Path(int x, int y, boolean active) {
		

	}

public int getX() {
	return x;
}

public void setX(int x) {
	this.x = x;
}

public int getY() {
	return y;
}

public void setY(int y) {
	this.y = y;
}

public boolean isActive() {
	return active;
}

public void setActive(boolean active) {
	this.active = active;
}




	
}
